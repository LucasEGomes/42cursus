#ifndef LIBFT_H
# define LIBFT_H
# include <stdlib.h>
# include <unistd.h>

/**
 * Checks for an alphabetic character using ASCII encoding
 * @param c Value of an unsigned char or EOF
 * @return Nonzero if c is alphabetic, and zero if not
*/
int	ft_isalpha(int c);
/**
 * Checks for an alphabetic character using ASCII encoding
 * @param c Value of an unsigned char or EOF
 * @return Nonzero if c is alphabetic, and zero if not
*/
int	ft_isdigit(int c);


#endif